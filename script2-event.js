function event_mouseover()
{
    document.getElementsByName("username")[0].style.backgroundColor = "lightblue";
    document.getElementsByName("password")[0].style.color = "black";
    document.getElementsByName("password")[0].style.backgroundColor = "lightblue";
    document.getElementsByName("username")[0].style.borderRadius = "10px";
    document.getElementsByName("password")[0].style.borderRadius = "10px";
}
